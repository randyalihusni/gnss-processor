@app.route('/upload/chunk', methods=['POST'])
def upload_chunk():
    """Terima satu chunk dari file besar. Dipanggil berulang dari JS.
    State disimpan di filesystem agar aman dengan multi-worker gunicorn."""
    upload_id    = request.form.get('upload_id', '')
    chunk_index  = int(request.form.get('chunk_index', 0))
    total_chunks = int(request.form.get('total_chunks', 1))
    filename     = request.form.get('filename', 'file.obs')
    file_type    = request.form.get('file_type', 'rover')
    token        = request.form.get('token', '')

    chunk = request.files.get('chunk')
    if not chunk:
        return jsonify({'error': 'No chunk data'}), 400

    # Buat direktori temp untuk upload_id ini
    chunk_dir = os.path.join(app.config['UPLOAD_FOLDER'], f'chunks_{upload_id}')
    os.makedirs(chunk_dir, exist_ok=True)

    # Simpan metadata ke file (bukan memory — aman untuk multi-worker)
    meta_path = os.path.join(chunk_dir, '_meta.json')
    if not os.path.exists(meta_path):
        import json as _json
        with open(meta_path, 'w') as mf:
            _json.dump({
                'filename':     filename,
                'total_chunks': total_chunks,
                'file_type':    file_type,
                'token':        token,
            }, mf)

    # Simpan chunk ke file
    chunk_path = os.path.join(chunk_dir, f'chunk_{chunk_index:05d}')
    chunk.save(chunk_path)

    # Hitung received dari filesystem (bukan memory)
    received = len([f for f in os.listdir(chunk_dir)
                    if f.startswith('chunk_') and not f.endswith('.tmp')])

    # Belum semua chunk diterima
    if received < total_chunks:
        return jsonify({'received': received, 'total': total_chunks, 'done': False})

    # Semua chunk diterima — gabungkan
    state = _chunk_uploads[upload_id]
    if file_type == 'rover':
        dest_token = str(__import__('uuid').uuid4())[:8]
        dest_dir   = os.path.join(app.config['UPLOAD_FOLDER'], f'pre_{dest_token}')
    else:
        dest_token = token
        if token and token in _uploaded_files:
            dest_dir = os.path.dirname(_uploaded_files[token]['rover'])
        else:
            dest_dir = os.path.join(app.config['UPLOAD_FOLDER'], f'base_{token}')
    os.makedirs(dest_dir, exist_ok=True)

    final_path = os.path.join(dest_dir, filename)
    with open(final_path, 'wb') as out:
        for i in range(total_chunks):
            cp = os.path.join(chunk_dir, f'chunk_{i:05d}')
            with open(cp, 'rb') as cf:
                out.write(cf.read())

    # Bersihkan chunks
    import shutil
    shutil.rmtree(chunk_dir, ignore_errors=True)
    del _chunk_uploads[upload_id]

    result = {'done': True, 'filename': filename, 'size': os.path.getsize(final_path)}

    if file_type == 'rover':
        _uploaded_files[dest_token] = {
            'rover': final_path, 'name': filename,
            'size':  os.path.getsize(final_path)
        }
        # Parse RINEX header untuk ant height
        ant_height = ''
        try:
            with open(final_path, 'r', errors='ignore') as fh:
                for line in fh.readlines()[:100]:
                    if 'END OF HEADER' in line: break
                    if 'ANTENNA: DELTA H/E/N' in line:
                        h = float(line[:14].strip())
                        if h >= 0: ant_height = f'{h:.3f}'
        except: pass
        result['token']      = dest_token
        result['ant_height'] = ant_height

    else:
        # Parse base header
        ant_height = ''; lat = ''; lon = ''; h_str = ''
        try:
            with open(final_path, 'r', errors='ignore') as fh:
                for line in fh.readlines()[:100]:
                    if 'END OF HEADER' in line: break
                    if 'ANTENNA: DELTA H/E/N' in line:
                        hv = float(line[:14].strip())
                        if hv >= 0: ant_height = f'{hv:.3f}'
                    if 'APPROX POSITION XYZ' in line:
                        import math
                        x=float(line[:14].strip()); y=float(line[14:28].strip()); z=float(line[28:42].strip())
                        a=6378137.0; e2=0.00669437999014
                        lon_r=math.atan2(y,x)
                        lat_r=math.atan2(z,math.sqrt(x*x+y*y))
                        for _ in range(10):
                            N=a/math.sqrt(1-e2*math.sin(lat_r)**2)
                            lat2=math.atan2(z+e2*N*math.sin(lat_r),math.sqrt(x*x+y*y))
                            if abs(lat2-lat_r)<1e-12: lat_r=lat2; break
                            lat_r=lat2
                        N=a/math.sqrt(1-e2*math.sin(lat_r)**2)
                        ht=math.sqrt(x*x+y*y)/math.cos(lat_r)-N
                        lat=f'{math.degrees(lat_r):.8f}'
                        lon=f'{math.degrees(lon_r):.8f}'
                        h_str=f'{ht:.4f}'
        except: pass
        if token in _uploaded_files:
            _uploaded_files[token]['base'] = final_path
        result.update({'ant_height': ant_height, 'lat': lat, 'lon': lon,
                        'h': h_str, 'path': final_path})

    return jsonify(result)

