def run_processing(job_id, rover, base, nav, sp3, params):
    job = jobs[job_id]
    job['status'] = 'processing'
    result_dir = os.path.join(app.config['RESULTS_FOLDER'], job_id)
    os.makedirs(result_dir, exist_ok=True)
    result_pos  = os.path.join(result_dir, 'result.pos')
    conf_path   = os.path.join(result_dir, 'rtklib.conf')

    # Write config file
    build_conf(params, conf_path)
    job['conf'] = conf_path

    # Download IGS nav jika belum ada
    if not nav or not os.path.exists(str(nav)):
        try:
            from igs_downloader import download_ephemeris
            eph_dir = os.path.join(result_dir, 'eph')
            eph = download_ephemeris(rover, eph_dir, 'broadcast', timeout_total=60)
            if eph.success and eph.nav_file:
                nav = eph.nav_file
                job['nav'] = os.path.basename(nav)
        except Exception as e:
            job['stderr'] = (job.get('stderr') or '') + f' [IGS error: {e}]'

    # Build command
    cmd = ['rnx2rtkp', '-k', conf_path, '-o', result_pos, rover]
    if base and os.path.exists(base): cmd.append(base)
    if nav and os.path.exists(str(nav)): cmd.append(nav)
    if sp3 and os.path.exists(sp3): cmd.append(sp3)

    try:
        proc = subprocess.run(cmd, capture_output=True, text=True, timeout=600)
        job['stdout'] = proc.stdout
        job['stderr'] = proc.stderr
        job['returncode'] = proc.returncode
        job['cmd'] = ' '.join(cmd)

        if proc.returncode == 0 and os.path.exists(result_pos):
            job['status'] = 'done'
            job['result_file'] = result_pos
            job['summary'] = parse_pos_file(result_pos)
        elif proc.returncode == 127 or not os.path.exists('/usr/local/bin/rnx2rtkp'):
            job['status'] = 'demo'
            job['summary'] = gen_demo(params.get('mode','static'))
            job['stderr'] = 'rnx2rtkp not installed — demo result. Config file tersimpan di: ' + conf_path
        else:
            job['status'] = 'error'
            job['error'] = proc.stderr or 'Processing failed (rc={})'.format(proc.returncode)
    except FileNotFoundError:
        job['status'] = 'demo'
        job['summary'] = gen_demo(params.get('mode','static'))
        job['stderr'] = 'rnx2rtkp not found. Config: ' + conf_path
    except subprocess.TimeoutExpired:
        job['status'] = 'error'; job['error'] = 'Timeout >600s'
    except Exception as e:
        job['status'] = 'error'; job['error'] = str(e)

    job['finished_at'] = datetime.now().isoformat()
    job['params'] = params
    save_jobs(jobs)  # persist ke disk


def parse_pos_file(filepath):
    rows = []
    try:
        with open(filepath) as f:
            for line in f:
                if line.startswith('%') or not line.strip(): continue
                p = line.split()
                if len(p) >= 8:
                    rows.append({'lat':float(p[2]),'lon':float(p[3]),'height':float(p[4]),'Q':int(p[5])})
    except: pass
    if not rows: return None
    fixed=[r for r in rows if r['Q']==1]
    lats=[r['lat'] for r in rows]; lons=[r['lon'] for r in rows]; hts=[r['height'] for r in rows]
    n=len(rows)
    def mn(v): return sum(v)/len(v)
    def sd(v): m=mn(v); return (sum((x-m)**2 for x in v)/len(v))**.5
    return {
        'total_epochs':n,'fixed_epochs':len(fixed),
        'fix_ratio':round(len(fixed)/n*100,1),
        'lat_mean':round(mn(lats),8),'lon_mean':round(mn(lons),8),'height_mean':round(mn(hts),4),
        'lat_std':round(sd(lats)*111000,5),'lon_std':round(sd(lons)*111000,5),'ht_std':round(sd(hts),5),
    }

def gen_demo(mode):
    import random
    return {
        'total_epochs':3600,'fixed_epochs':3421 if mode=='static' else 1800,
        'fix_ratio':95.0 if mode=='static' else 50.0,
        'lat_mean':round(-7.1665+random.uniform(-0.0001,0.0001),8),
        'lon_mean':round(112.6508+random.uniform(-0.0001,0.0001),8),
        'height_mean':round(12.345+random.uniform(-0.5,0.5),4),
        'lat_std':round(random.uniform(0.002,0.005),5),
        'lon_std':round(random.uniform(0.002,0.005),5),
        'ht_std':round(random.uniform(0.004,0.010),5),
        'demo':True,
    }

