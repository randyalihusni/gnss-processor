@app.route('/parse_header', methods=['POST'])
def parse_header():
    """Parse RINEX OBS header — server fallback untuk file binary/compressed."""
    f = request.files.get('obs_file')
    if not f:
        return jsonify({'error': 'No file'}), 400

    tmp = os.path.join(app.config['UPLOAD_FOLDER'], f'tmp_{f.filename}')
    f.save(tmp)

    result = {
        'antHeight':  '',
        'antType':    '',
        'markerName': '',
        'approxPos':  None,
        'recType':    '',
        'interval':   '',
    }

    try:
        # Try plain text first
        try:
            with open(tmp, 'r', errors='ignore') as fh:
                lines = fh.readlines()
        except Exception:
            lines = []

        # If file is compressed (.Z or .gz), try decompress
        if not lines or (lines and len(lines[0]) < 5):
            import gzip
            try:
                with gzip.open(tmp, 'rt', errors='ignore') as fh:
                    lines = fh.readlines()
            except Exception:
                pass

        for line in lines[:200]:  # header max ~200 baris
            if 'END OF HEADER' in line:
                break
            if 'ANTENNA: DELTA H/E/N' in line:
                try:
                    h = float(line[:14].strip())
                    if h >= 0:
                        result['antHeight'] = f'{h:.3f}'
                except ValueError:
                    pass
            if 'ANT # / TYPE' in line:
                t = line[20:40].strip()
                if t:
                    result['antType'] = t
            if 'MARKER NAME' in line:
                m = line[:60].strip()
                if m:
                    result['markerName'] = m
            if 'REC # / TYPE / VERS' in line:
                r = line[20:40].strip()
                if r:
                    result['recType'] = r
            if 'INTERVAL' in line:
                try:
                    iv = float(line[:10].strip())
                    result['interval'] = f'{iv:.3f}'
                except ValueError:
                    pass
            if 'APPROX POSITION XYZ' in line:
                try:
                    x = float(line[0:14].strip())
                    y = float(line[14:28].strip())
                    z = float(line[28:42].strip())
                    result['approxPos'] = {'x': x, 'y': y, 'z': z}
                except ValueError:
                    pass

    except Exception as e:
        result['error'] = str(e)
    finally:
        if os.path.exists(tmp):
            os.remove(tmp)

    return jsonify(result)


