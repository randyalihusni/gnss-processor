@app.route('/upload/process', methods=['POST'])
def upload_process():
    """Start processing using pre-uploaded files."""
    import uuid as _uuid
    data = request.get_json()
    tokens = data.get('tokens', [])
    params = data.get('params', {})
    base_path = data.get('base_path', '')
    eph_source = data.get('ephemeris_source', 'igs')
    eph_type   = data.get('ephemeris_type', 'broadcast')

    if not tokens:
        return jsonify({'error': 'Tidak ada file yang diupload'}), 400

    if len(tokens) == 1:
        # Single processing
        token = tokens[0]
        info  = _uploaded_files.get(token, {})
        rover = info.get('rover', '')
        if not rover or not os.path.exists(rover):
            return jsonify({'error': 'File rover tidak ditemukan'}), 400

        jid = _uuid.uuid4().hex[:8]
        ant_heights = data.get('ant_heights', ['0'])
        params['ant_ht_rover'] = ant_heights[0] if ant_heights else '0'

        # Nav file
        nav = None
        if eph_source == 'manual':
            nav = info.get('nav', '')
        base = base_path or info.get('base', '')

        result_dir = os.path.join(app.config['RESULTS_FOLDER'], jid)
        os.makedirs(result_dir, exist_ok=True)

        jobs[jid] = {
            'id': jid, 'status': 'queued', 'mode': params.get('mode','static'),
            'rover': os.path.basename(rover),
            'base':  os.path.basename(base) if base else None,
            'nav':   os.path.basename(nav) if nav else 'IGS Auto',
            'created_at': datetime.now().isoformat(),
            'summary': None, 'result_file': None, 'error': None,
            'stdout': '', 'stderr': '', 'params': params,
        }
        save_jobs(jobs)

        import threading
        threading.Thread(
            target=run_processing,
            args=(jid, rover, base or None, nav or '', None, params),
            daemon=True
        ).start()
        return jsonify({'job_id': jid, 'type': 'single'})

    else:
        # Batch processing
        from batch_processor import BatchJob, BatchPoint, batch_jobs, name_from_file
        bid = _uuid.uuid4().hex[:8]
        result_dir = os.path.join(app.config['RESULTS_FOLDER'], f'batch_{bid}')
