@app.route('/igs/check', methods=['POST'])
def igs_check():
    """Cek tanggal dari OBS file dan ketersediaan ephemeris di IGS."""
    f = request.files.get('obs_file')
    if not f: return jsonify({'error': 'No file'}), 400
    tmp = os.path.join(app.config['UPLOAD_FOLDER'], f.filename)
    f.save(tmp)
    try:
        from igs_downloader import parse_rinex_date, doy_from_date, gps_week_dow
        from datetime import datetime as dt2
        date = parse_rinex_date(tmp)
        if not date:
            return jsonify({'error': 'Tidak bisa membaca tanggal dari file OBS'})
        y, m, d = date
        doy = doy_from_date(y, m, d)
        week, dow = gps_week_dow(y, m, d)
        age = (dt2.now() - dt2(y,m,d)).days
        return jsonify({
            'date':       f'{y}-{m:02d}-{d:02d}',
            'doy':        doy,
            'gps_week':   week,
            'gps_dow':    dow,
            'age_days':   age,
            'broadcast_available': True,
            'rapid_available':     age >= 2,
            'final_available':     age >= 14,
        })
    finally:
        if os.path.exists(tmp): os.remove(tmp)


