def _cleanup_old_files(max_age_hours=48):
    """Hapus folder upload/result/chunk lama (>48 jam)."""
    now = _time.time()
    max_age = max_age_hours * 3600
    for base_dir in [UPLOAD_FOLDER, RESULTS_FOLDER]:
        if not os.path.exists(base_dir): continue
        for name in os.listdir(base_dir):
            path = os.path.join(base_dir, name)
            if not os.path.isdir(path): continue
            if now - os.path.getmtime(path) > max_age:
                try: _shutil.rmtree(path)
                except: pass
    # Cleanup token store
    expired = [k for k, v in _uploaded_files.items()
               if not os.path.exists(v.get('rover', ''))]
    for k in expired: _uploaded_files.pop(k, None)
    expired_c = [k for k, v in _chunk_uploads.items()
                 if not os.path.exists(v.get('chunk_dir', ''))]
    for k in expired_c: _chunk_uploads.pop(k, None)

def _cleanup_loop():
    while True:
        _time.sleep(3600)
        try: _cleanup_old_files()
        except: pass

_threading.Thread(target=_cleanup_loop, daemon=True).start()

